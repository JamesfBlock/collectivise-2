class SalesController < ApplicationController
  def index         # GET /sales
  end

  def show          # GET /sales/:id
  end

  def new           # GET /sales/new
  end

  def create        # POST /sales
  end

  def edit          # GET /sales/:id/edit
  end

  def update        # PATCH /sales/:id
  end

  def destroy       # DELETE /sales/:id
  end
end
